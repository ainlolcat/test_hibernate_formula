DROP TABLE IF EXISTS TEST_STORAGES;
DROP TABLE IF EXISTS TEST_ITEMS;
DROP TABLE IF EXISTS TEST_RELATIONS;


--Storages
CREATE TABLE TEST_STORAGES
(
  STORAGE_ID NUMERIC,
  NAME VARCHAR(200),
  CONSTRAINT STORAGES_PK PRIMARY KEY (STORAGE_ID)
);

--Items
CREATE TABLE TEST_ITEMS
(
  ITEM_ID NUMERIC,
  NAME VARCHAR(200),
  STORAGE_ID NUMERIC,
  CONSTRAINT ITEMS_PK PRIMARY KEY (ITEM_ID)
);

--Relations
CREATE TABLE TEST_RELATIONS
(
  RELATION_ID NUMERIC,
  STORAGE_ID NUMERIC,
  FROM_ITEM_ID NUMERIC,
  TO_ITEM_ID NUMERIC
);